// Deep-widen: turn literal string types into `string` so both he and en satisfy the type,
// but keep function signatures and Record<string,string> intact.
type DeepStringify<T> = T extends (...args: infer A) => infer R
  ? (...args: A) => R
  : T extends Record<string, string>
    ? Record<string, string>
    : T extends object
      ? { [K in keyof T]: DeepStringify<T[K]> }
      : T extends string
        ? string
        : T;

export type Translations = DeepStringify<typeof he>;

